def add(a: float, b: float) -> float:
    """
    A type-annotated function add
    that takes a float a and a float
    b as arguments and returns their
    sum as a float

    Args:
        a (float)
        b (float)

    Returns:
        sum of a and b (float)
    """
    return (a+b)
