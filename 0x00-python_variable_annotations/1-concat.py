#!/usr/bin/env python3
"""
Basic annotations - concat module
"""


def concat(str1: str, str2: str) -> str:
    """
    A type-annotated function concat that
    takes a string str1 and a string str2
    as arguments and returns a concatenated string

    Args:
        str1 (str)
        str2 (str)

    Returns:
        concatenated string (str)
    """
    return (str1 + str2)
